#pragma once

// 0x<error level bit><system><id bits(2)>
// System:
//      GLFW: 0
//      VULKAN: 1
//      GLM: 2
//      VKMEMALLOC: 4
//      DRAGON: F
enum DgResult_T {
    // Success representation
    DG_SUCCESS = 0x0000,
    // Critical errors, probably due to hardware limitations
    DG_GLFW_INIT_FAILED = 0xF000,
    DG_GLFW_VULKAN_SUPPORT_FAILED = 0xF001,
    DG_GLFW_VULKAN_EXTENSIONS_NOT_FOUND = 0xF002,
    DG_GLFW_CREATE_WINDOW_SURFACE_FAILED = 0xF03,

    DG_VULKAN_CREATE_INSTANCE_FAILED = 0xF100,
    DG_VULKAN_DEBUG_UTILS_INSTANCE_CREATION_FAILED = 0xF101,
    DG_VULKAN_SUPPORTED_GPUS_UNAVAILABLE = 0xF102,
    DG_VULKAN_CREATE_DEVICE_FAILED = 0xF103,
    DG_VULKAN_GRAPHICS_QUEUE_NOT_FOUND = 0xF104,
    DG_VUlKAN_NO_SWAP_CHAIN_FORMAT_MODES_FOUND = 0xF105,
    DG_VULKAN_NO_SWAP_CHAIN_PRESENT_MODES_FOUND = 0xF106,
    DG_VULKAN_PRESENT_QUEUE_NOT_FOUND = 0xF107,
    DG_VULKAN_SWAP_CHAIN_CREATION_FAILED = 0xF108,
    DG_VULKAN_CREATE_IMAGE_VIEW_FAILED = 0xF109,
    DG_VULKAN_CREATE_SHADER_MODULE_FAILED = 0xF10A,
    DG_VULKAN_CREATE_PIPELINE_LAYOUT_FAILED = 0xF10B,
    DG_VULKAN_CREATE_RENDER_PASS_FAILED = 0xF10C,
    DG_VULKAN_CREATE_GRAPHICS_PIPELINE_FAILED = 0xF10D,
    DG_VULKAN_CREATE_FRAMEBUFFER_FAILED = 0xF10E,
    DG_VULKAN_CREATE_COMMAND_POOL_FAILED = 0xF10F,
    DG_VULKAN_ALLOCATE_COMMAND_BUFFERS_FAILED = 0xF110,
    DG_VULKAN_CREATE_SEMAPHORE_FAILED = 0xF111,
    DG_VULKAN_CREATE_FENCE_FAILED = 0xF112,
    DG_VULKAN_ALLOCATE_MEMORY_FAILED = 0xF113,
    DG_VULKAN_CREATE_BUFFER_FAILED = 0xF114,
    DG_VULKAN_CREATE_DESCRIPTOR_SET_LAYOUT_FAILED = 0xF115,

    DG_VKMEMALLOC_CREATE_ALLOCATOR_FAILED = 0xF400,
    DG_VKMEMALLOC_CREATE_BUFFER_FAILED = 0xF401,

    DG_UNKNOWN_CRITICAL_ERROR = 0xFF00,
    // Errors, but mostly software ones, probably fixable
    DG_VULKAN_BEGIN_COMMAND_BUFFER_FAILED = 0x8100,
    DG_VULKAN_END_COMMAND_BUFFER_FAILED = 0x8101,
    DG_VULKAN_QUEUE_SUBMIT_FAILED = 0x8102,

    DG_UNKNOWN_ERROR = 0x8F00,
    DG_ARGUMENT_INVALID = 0x8F01,
    DG_CAMERA_UNLOCKED = 0x8F02,
    // Warnings, not too bad
    DG_UNKNOWN_WARNING = 0x4F00,
    // Statuses, these are fine, will probably occur in regular usage
    DG_UNKNOWN_STATUS = 0x2F00,
    DG_SWAPCHAIN_RECREATED = 0x2F01,
    // Verbose output, probably not worth seeing
    DG_UNKNOWN_VERBOSE = 0x1F00
};

typedef enum DgResult_T DgResult;